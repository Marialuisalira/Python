
# Variáveis em Python
# Não há necessidade de declrá-las no início de progamas
# Basta criar no meio do algoritmo com atribuições 

teste = "Maria Luisa"
print(teste) # Imprime  a string "Maria Luisa"
print(type(teste)) # Imprime o tipo de varíavel, string
teste = 10 
print(teste) # Imprime o número 10
print(type(teste)) # Imprime o tipo de varíavel, que int
teste = 1.74
print(teste) # Imprime o número 1.74
print(type(teste)) # Imprime o tipo de varíavel, que é float
teste = True 
print(teste) # Imprime o valor True
print(type(teste)) # Imprime o tipo da varíavel, que é bool

